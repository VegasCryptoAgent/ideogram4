/**
 * Get brokers sorted by priority (ascending — 1 = highest priority first).
 */
export function getBrokersByPriority(): typeof BROKERS {
  return [...BROKERS].sort((a, b) => a.priority - b.priority);
}

/**
 * Get brokers filtered by category.
 */
export function getBrokersByCategory(category: string): typeof BROKERS {
  return BROKERS.filter((b) => b.category === category);
}

/**
 * Get the top N highest-priority brokers.
 */
export function getTopBrokers(n = 50): typeof BROKERS {
  return getBrokersByPriority().slice(0, n);
}
